// Helper: get all brand names for the first dropdown
export const BRAND_OPTIONS = PANEL_BRANDS.map(b => ({
  value: b.brandSlug,
  label: b.brandName,
  tier: b.tier,
}))

// Helper: get models for a given brand
export function getModelsForBrand(brandSlug: string): { value: string; label: string }[] {
  const brand = PANEL_BRANDS.find(b => b.brandSlug === brandSlug)
  if (!brand) return []
  return brand.models.map(m => ({
    value: m.modelSlug,
    label: m.modelName,
  }))
}

// Helper: find a specific model
export function findModel(brandSlug: string, modelSlug: string): PanelModel | null {
  const brand = PANEL_BRANDS.find(b => b.brandSlug === brandSlug)
  if (!brand) return null
  return brand.models.find(m => m.modelSlug === modelSlug) || null
}

// Helper: find a brand record
export function findBrand(brandSlug: string): PanelBrand | null {
  return PANEL_BRANDS.find(b => b.brandSlug === brandSlug) || null
}
