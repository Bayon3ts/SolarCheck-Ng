export interface PanelModel {
  modelSlug: string
  modelName: string     // e.g. "Tiger Neo N-Type 550W"
  watts: number         // e.g. 550
  efficiency: number    // e.g. 22.04 (%)
  cellType: string      // 'TOPCon' | 'PERC' | 'HJT' | 'Mono-PERC' | 'Polycrystalline'
  type: string          // 'Monocrystalline' | 'Polycrystalline'
  warranty: number      // years — product warranty
  performanceWarranty: number // years e.g. 25
  degradationPerYear: number  // % e.g. 0.40
  outputAfter25Years: number  // % e.g. 84.8
  priceMin: number      // ₦ per panel
  priceMax: number      // ₦ per panel
  tempCoefficient: number // Pmax %/°C e.g. -0.30
  bifacial: boolean
  halfCut: boolean
  origin: string        // 'CN' | 'US' | 'KR' | 'NG'
  tier: 'tier1' | 'tier2' | 'premium' | 'budget'
  nigeriaAvailability: 'widely-available' | 'available' | 'limited'
  bestFor: string       // one-liner
}

